int cnt = 0;
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	if (htim->Instance == TIM4)
	{
		cnt++;
		if (now == 0)
		{
			if (cnt >2000)
			{
				cnt = 0;
				htim->Instance->CCR1 = 10;
				now = 1;
			}
		}
		else if (now == 1)
		{
			if(cnt>Servo1_Pow)
			{
		//		if (Servo1_Pow == 70)Servo1_Pow = 50;
			//	Servo1_Pow++;
				cnt = 0;
				htim->Instance->CCR1 = 0;
				now = 0;
			}
		}
	}
}
